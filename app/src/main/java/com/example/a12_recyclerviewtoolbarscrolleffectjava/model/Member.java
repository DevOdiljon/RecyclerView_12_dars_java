package com.example.a12_recyclerviewtoolbarscrolleffectjava.model;

public class Member {
    String tv_name;
    String tv_tel_name;
    public Member(String tv_name, String tv_tel_name){
        this.tv_name = tv_name;
        this.tv_tel_name = tv_tel_name;
    }

    public String getTv_name() {
        return tv_name;
    }

    public String getTv_tel_name() {
        return tv_tel_name;
    }
}
